package Q4;

public class Student
{
    String uni = ""; // initially unknown
    int id = 0; // initially no id

    int getIdentifier()
    {
        return id;
    }

    void setIdentifier(int i)
    {
        this.id = i;
    }

    String getUni()
    {
        return uni;
    }

    void setUni(String u)
    {
        this.uni = u;
    }

}
